        var screenSpecifier = new SpriteSpecifier.Rsi(screenRsiPath, "screenStatic");
        ScreenOverlay.Texture = _spriteSystem.Frame0(screenSpecifier);

        // === LAYOUT ===
        // screenStatic.png is 128x164 px, rendered at 2x = 256x328.
        // computerBack.png is 159x271 px, rendered at 2x = 318x542.
