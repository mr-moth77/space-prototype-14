    protected override void FrameUpdate(FrameEventArgs args)
    {
        base.FrameUpdate(args);

        // Update ECG line animation
        _ecgGraph.UpdateTime(args.DeltaSeconds);

        // Flash bleeding warning icon if patient is bleeding
        if (_isBleeding)
        {
            _bleedFlashTimer += args.DeltaSeconds;
            if (_bleedFlashTimer >= 0.4)
            {
                _bleedFlashTimer = 0.0;
                BleedingWarningIcon.Visible = !BleedingWarningIcon.Visible;
            }
        }
        else
        {
            BleedingWarningIcon.Visible = false;
        }
    }

    public void UpdateState(ScavImplantUpdateMessage msg)
    {
        // 1. Header & stats
        ExperimentLabel.Text = $"{msg.SpeciesName.ToUpper()}";
        StatsLabel.Text = $"{msg.Height}cm | {msg.Age}y | #{msg.EntityName.GetHashCode() & 0xFFFF:X4}";

        // 2. Brain health
        BrainHealthBar.Value = (int)(msg.BrainHealth * 100f);
        BrainHealthText.Text = $"{(int)(msg.BrainHealth * 100f)}";

        // 3. Overall health percentage
        TotalHealthText.Text = $"{(int)msg.HealthPercent}%";

        // 4. Blood
        BloodText.Text = $"{msg.BloodAmount * 5.0f:F2}L | {(int)(msg.BloodAmount * 100f)}%";
        _isBleeding = msg.IsBleeding;

        // 5. Pain
        PainText.Text = $"{(int)msg.PainPercent}%";

        // 6. Temperature
        TemperatureText.Text = $"{msg.Temperature:F1}'c";

        // 7. Water & Food
        HungerBar.Value = (int)(msg.HungerPercent * 100f);
        ThirstBar.Value = (int)(msg.ThirstPercent * 100f);

        // 8. Radiation
        RadText.Text = $"{msg.Radiation:F1}gy";

        // 9. Pulse
        PulseText.Text = $"{msg.Pulse} bpm";
        _ecgGraph.UpdateBpm(msg.Pulse);

        // 10. Doll health
        var targetEntity = _entityManager.GetEntity(msg.TargetEntity);
        if (_entityManager.HasComponent<HumanoidAppearanceComponent>(targetEntity))
        {
            var dollIcon = SetupIcon(msg.BodyParts, msg.BleedingParts);
            if (dollIcon.HasValue)
            {
                DollSpriteView.SetEntity(dollIcon.Value);
                DollSpriteView.Visible = true;
            }
        }
        else
        {
            DollSpriteView.SetEntity(targetEntity);
            DollSpriteView.Visible = true;
        }
    }

    private EntityUid? SetupIcon(Dictionary<TargetBodyPart, WoundableSeverity>? body, Dictionary<TargetBodyPart, bool> bleeding)
    {
        if (body is null)
            return null;

        if (!_entityManager.Deleted(_spriteViewEntity))
            _entityManager.QueueDeleteEntity(_spriteViewEntity);

        _spriteViewEntity = _entityManager.Spawn(_bodyView);

        if (!_entityManager.TryGetComponent<SpriteComponent>(_spriteViewEntity, out var sprite))
            return null;

        int layer = 0;
        foreach (var (bodyPart, integrity) in body)
        {
            string enumName = Enum.GetName(typeof(TargetBodyPart), bodyPart) ?? "Unknown";
            int enumValue = (int) integrity;
            var baseRsiPath = new ResPath($"/Textures/_Shitmed/Interface/Targeting/Status/{enumName.ToLowerInvariant()}.rsi");
            var rsi = new SpriteSpecifier.Rsi(baseRsiPath, $"{enumName.ToLowerInvariant()}_{enumValue}");
            CreateOrAddToLayer(sprite, rsi, layer);
            layer++;

            if (bleeding.TryGetValue(bodyPart, out var isBleeding) && isBleeding)
            {
                var bleedRsi = new SpriteSpecifier.Rsi(baseRsiPath, $"{enumName.ToLowerInvariant()}_bleed");
                CreateOrAddToLayer(sprite, bleedRsi, layer);
                layer++;
            }
        }
        return _spriteViewEntity;
    }

    private void CreateOrAddToLayer(SpriteComponent sprite, SpriteSpecifier rsi, int layer)
    {
        if (!sprite.TryGetLayer(layer, out _))
            sprite.AddLayer(_spriteSystem.Frame0(rsi));
        else
            sprite.LayerSetTexture(layer, _spriteSystem.Frame0(rsi));

        sprite.LayerSetScale(layer, new Vector2(2f, 2f));
    }

    protected override void Dispose(bool disposing)
    {
        base.Dispose(disposing);
        if (disposing && !_entityManager.Deleted(_spriteViewEntity))
        {
            _entityManager.QueueDeleteEntity(_spriteViewEntity);
        }
    }
}

/// <summary>
/// Custom Control that draws a realistic animated ECG heartbeat waveform.
/// Uses a proper PQRST complex with smooth transitions between waves.
/// The waveform scrolls left-to-right like a real cardiac monitor.
/// </summary>
public sealed class ECGGraph : Control
{
    private float _time;
    private int _bpm = 75;
    private float _accumulatedScroll;
    private readonly List<float> _history = new();

    // Glow effect: we draw the line multiple times with decreasing alpha and increasing width
    private static readonly Color LineColor = Color.FromHex("#18E298");
    private static readonly Color GlowColor = new(0.094f, 0.886f, 0.596f, 0.15f);

    public void UpdateBpm(int bpm)
    {
        _bpm = bpm;
    }

    public void UpdateTime(float delta)
    {
        var beatInterval = 60.0f / MathF.Max(1.0f, _bpm);

        // Scroll speed: 50 pixels per second (slower for a smooth medical monitor feel)
        var pixelsToScroll = delta * 50.0f;
        _accumulatedScroll += pixelsToScroll;
        var scrollCount = (int) _accumulatedScroll;
        _accumulatedScroll -= scrollCount;

        // Remove old points from the left
        if (scrollCount > 0)
        {
            if (_history.Count > 0)
            {
                if (scrollCount >= _history.Count)
                    _history.Clear();
                else
                    _history.RemoveRange(0, scrollCount);
            }
        }

        // Fill in new points on the right
        var bounds = PixelSizeBox;
        var targetCount = bounds.Width > 0 ? (int)bounds.Width : 108;

        while (_history.Count < targetCount)
        {
            _time += (1.0f / 50.0f); // matches scroll speed
            var t = _time % beatInterval;
            var normalized = t / beatInterval; // 0..1 within one beat cycle
            var yOffset = GetECGValue(normalized);
            _history.Add(yOffset);
        }
    }

    /// <summary>
    /// Attempt to produce a realistic PQRST ECG waveform.
    /// normalized = 0..1 representing one full cardiac cycle.
    /// Returns y-offset in pixels (positive = upward deflection).
    /// </summary>
    private static float GetECGValue(float normalized)
    {
        // A typical ECG cycle timing (as fraction of beat):
        //   P wave:   0.05 - 0.15   (atrial depolarization, small upward bump)
        //   PR seg:   0.15 - 0.20   (flat baseline)
        //   Q wave:   0.20 - 0.23   (small downward dip)
        //   R wave:   0.23 - 0.28   (large upward spike)
        //   S wave:   0.28 - 0.32   (downward dip below baseline)
        //   ST seg:   0.32 - 0.40   (return to baseline)
        //   T wave:   0.40 - 0.55   (repolarization, medium upward bump)
        //   Rest:     0.55 - 1.00   (flat baseline)

        float t = normalized;

        // P wave: gentle sine bump
        if (t >= 0.05f && t < 0.15f)
        {
            float phase = (t - 0.05f) / 0.10f;
            return 2.5f * MathF.Sin(phase * MathF.PI);
        }

        // Q wave: small downward notch
        if (t >= 0.20f && t < 0.23f)
        {
            float phase = (t - 0.20f) / 0.03f;
            return -2.0f * MathF.Sin(phase * MathF.PI);
        }

        // R wave: sharp upward spike
        if (t >= 0.23f && t < 0.28f)
        {
            float phase = (t - 0.23f) / 0.05f;
            return 12.0f * MathF.Sin(phase * MathF.PI);
        }

        // S wave: downward dip
        if (t >= 0.28f && t < 0.32f)
        {
            float phase = (t - 0.28f) / 0.04f;
            return -4.0f * MathF.Sin(phase * MathF.PI);
        }

        // T wave: medium upward bump
        if (t >= 0.40f && t < 0.55f)
        {
            float phase = (t - 0.40f) / 0.15f;
            return 4.0f * MathF.Sin(phase * MathF.PI);
        }

        // Baseline
        return 0.0f;
    }

    protected override void Draw(DrawingHandleScreen handle)
    {
        base.Draw(handle);

        var bounds = PixelSizeBox;
        if (_history.Count < 2)
            return;

        var centerY = bounds.Height / 2f;

        // Draw a subtle glow layer first (thicker, more transparent)
        for (int x = 1; x < _history.Count && x < (int)bounds.Width; x++)
        {
            var p1 = new Vector2(x - 1, centerY - _history[x - 1]);
            var p2 = new Vector2(x, centerY - _history[x]);
            handle.DrawLine(p1, p2, GlowColor);
            // Offset lines for thickness
            handle.DrawLine(p1 + new Vector2(0, 1), p2 + new Vector2(0, 1), GlowColor);
            handle.DrawLine(p1 - new Vector2(0, 1), p2 - new Vector2(0, 1), GlowColor);
        }

        // Draw the main crisp line
        for (int x = 1; x < _history.Count && x < (int)bounds.Width; x++)
        {
            var p1 = new Vector2(x - 1, centerY - _history[x - 1]);
            var p2 = new Vector2(x, centerY - _history[x]);
            handle.DrawLine(p1, p2, LineColor);
        }
    }
}
